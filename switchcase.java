public class switchcase {
    public static void main(String[] args) {
        int year=1;
      switch(year){
        case 1: System.out.println("year 1");
        break;
          case 2: System.out.println("year 2");
          break;
            case 3: System.out.println("year 3");
            break;
        default: System.out.println("none of them");
      }
    }
}
